# loops are used to repeat a block of code multiple times
# There are two types of loops in Python, for and while loop
# types of iteration:indefinite iteration and definite iteration
# Indefinite iteration: when the number of times the loop is executed depends on how many times a condition is met.
# Definite iteration: when the number of times the loop will be executed is defined in advance.

fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    # print(f"I like {fruit}")
    print("I like " + fruit)
    
